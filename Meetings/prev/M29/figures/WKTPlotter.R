POLYGON ((1964870.32134375 572835.0190625, 1965872.2946875 572835.0190625, 1965872.2946875 574100.413, 1964870.32134375 574100.413, 1964870.32134375 572835.0190625)),
POLYGON ((1963868.348 571569.625125, 1964870.32134375 571569.625125, 1964870.32134375 572835.0190625, 1963868.348 572835.0190625, 1963868.348 571569.625125)),
POLYGON ((1964870.32134375 571569.625125, 1965872.2946875 571569.625125, 1965872.2946875 572835.0190625, 1964870.32134375 572835.0190625, 1964870.32134375 571569.625125)),
